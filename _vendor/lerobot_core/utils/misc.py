"""Small console helpers extracted from ``lerobot.utils.utils``.

Only the two helpers used by the vendored motors bus are reproduced here, so we
avoid importing the full ``lerobot.utils.utils`` module and its many
dependencies.
"""

import platform
import select
import sys


def enter_pressed() -> bool:
    if platform.system() == "Windows":
        import msvcrt

        if msvcrt.kbhit():
            key = msvcrt.getch()
            return key in (b"\r", b"\n")  # enter key
        return False
    else:
        return select.select([sys.stdin], [], [], 0)[0] and sys.stdin.readline().strip() == ""


def move_cursor_up(lines):
    """Move the cursor up by a specified number of lines."""
    print(f"\033[{lines}A", end="")
